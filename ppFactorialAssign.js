function factorialNumber(num) {
    console.log(`Given number is : ${num}`);

    var fact = 1; //5
    for (var index = num; index >= 1; index--) {
        //     5
        fact = fact * index; // 5 * 4
        
        }
        console.log(`${fact}`);
    }
  

